    </p>
    <p>
      Every object requires storage.
    </p>
    <p>
      You reserve storage for objects in a process called allocation.
    </p>
    <p>
      When youre done with an object, you release the objects
    </p>
    <p>
      storage in a process called deallocation.
    </p>
    <p>
      An objects storage duration begins when the object is allocated and
    </p>
    <p>
      ends when the object is deallocated.
    </p>
    <p>
      The lifetime of an object is a runtime property that is bound by the objects storage duration.
    </p>
    <p>
      &#xa0;An objects lifetime begins once its constructor completes, and it ends just before a destructor is invoked.
    </p>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      Each object passes through the following stages:
    </p>
    <p>
      1. The objects storage duration begins, and storage is allocated.
    </p>
    <p>
      2. The objects constructor is called.
    </p>
    <p>
      3. The objects lifetime begins.
    </p>
    <p>
      4. You can use the object in your program.
    </p>
    <p>
      5. The objects lifetime ends.
    </p>
    <p>
      6. The objects destructor is called.
    </p>
    <p>
      7. The objects storage duration ends, and storage is deallocated.
    </p>
